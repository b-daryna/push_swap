#include "libft.h"

int		ft_max(int *tab, unsigned int len)
{
	unsigned int i;

	i = 1;
	while (i < len)
	{
		if (tab[i] - tab[i - 1] < 0)
			tab[i] = tab[i - 1];
		i++;
	}
	return (tab[i - 1]);
}
