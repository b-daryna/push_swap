#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int num;
	int negative;

	i = 0;
	num = 0;
	negative = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || \
		str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			negative = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i])
	{
		num *= 10;
		num += str[i] - '0';
		i++;
	}
	num = negative * num;
	return (num);
}
